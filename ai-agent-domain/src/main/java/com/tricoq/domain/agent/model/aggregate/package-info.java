/**
 * 聚合根对象:用于“写”场景，封装行为和不变式（只能绑定一个模型、去重、后续可加校验/状态流转）。应用/领域服务对它发命令，仓储持久化它
 * 1. 聚合实体和值对象
 * 2. 聚合是聚合的对象，和提供基础处理对象的方法。但不建议在聚合中引入仓储和接口来做过大的逻辑。而这些复杂的操作应该放到service中处理
 * 3. 对象名称 XxxAggregate
 *
 * 把一组强一致性的数据和行为包成一个单元（Aggregate），外部只能通过根操作，避免“绕过领域逻辑直接改子对象”。
 * 在聚合内部集中校验不变式（状态流转、额度、唯一性等），减少分散在服务/Controller 的 if/else。
 * 定义边界：根内对象之间强一致，跨聚合靠领域事件/应用服务协作。
 * 简化仓储：一个聚合根对应一个仓储接口，持久化/事务的粒度清晰。
 * 方便演进：业务规则变化时只改聚合内部，不让基础设施层的 DO/VO 曝露给业务。
 *
 * 注意：聚合根作为一个整体进行变动----聚合以提交的数据为准
 * 比如说聚合根中集合为空，对应就需要把数据删掉
 */
